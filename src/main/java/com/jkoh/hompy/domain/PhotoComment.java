package com.jkoh.hompy.domain;

public class PhotoComment {
	
	private String num;
	private String comment;
	private String writer;
	private String photos_comments_id;
	
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter(String writer) {
		this.writer = writer;
	}
	public String getPhotos_comments_id() {
		return photos_comments_id;
	}
	public void setPhotos_comments_id(String photos_comments_id) {
		this.photos_comments_id = photos_comments_id;
	}
	
}
